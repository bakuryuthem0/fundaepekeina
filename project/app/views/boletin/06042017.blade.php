                        <h2 class="hist-title">Miguel Martínez crece y multiplica su bondad de la mano de Funda Epékeina </h2>
                    </div>
                    <hr>
                    <div class="text-justify">
                        De los 19 años de vida de
Miguel Arturo Martínez, 7 ha participado como voluntario permanente de Funda
Epékeina concentrando sus esfuerzos en ofrecer alegría, ayuda y aprendizaje a
los más necesitados. 

Cuando apenas tenía 9 años
fue monaguillo de la parroquia caraqueña El Salvador, ubicada  en Las Acacias y posteriormente apoyó la
misión evangelizadora que llegó hasta Terrazas del Alba, donde se iniciaron
formalmente las labores como fundación.  A
la par de su crecimiento fue asumiendo otras responsabilidades como recreador y
miembro del equipo de logística. 

Miguelito -como
cariñosamente muchos lo llaman sin importar su edad ni tamaño-  es un muchacho ejemplar y comprometido. Su
voz grave de significativa proyección, además de su espíritu alegre y hasta su
gran tamaño, lo dibujan como un gran líder del equipo epékeino.

Considera que el ser
voluntario es una forma de vida que pone al ser humano en constante
aprendizaje. “No hay otra cosa mejor que ser canal de esa mano amiga para darle
de comer y enseñar a leer o escribir a quienes no tienen nada”, explicó con
mucho entusiasmo.

Sus anécdotas las describe
como infinitas historias. Sin embargo, recuerda la frase de una joven
beneficiaria, quien se preparaba para asumir la maternidad en tiempos de
adolescencia. “Siempre hay que mirar hacia delante y nunca para atrás”, fueron
las palabras que en el contexto de lo que ocurría, marcaron la vida de Miguel
Arturo. – Eso me ha servido para tomar lo positivo de las situaciones difíciles
y seguir adelante-, dijo.
