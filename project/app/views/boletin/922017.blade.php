                        <span class="text-pink"><strong>Verónica Macedo,</strong></span>
                    </h1>   
                    <h2>
                        <span class="text-blue"><strong>gota de vida y acción de <span class="bolder">Funda Epékeina</span></strong></span>
                    </h2>
                    <p class="text-justify">
                        Su nombre es Verónica Macedo, en su historia de vida guarda apreciadas vivencias cargadas de risas, alegrías y en el fondo, mucha nostalgia. Su historia tomó un giro hace 7 años, inicios del 2010, cuando en compañía del padre Honegger Molina, párroco de la parroquia El Salvador en Las Acacias, para ese entonces, iniciaron labores en la Fundación Epékeina. Su deseo de hacer el bien, ofrecer protección al desasistido y contribuir con el bienestar de los más pequeños, sirvió para forjar triunfos en una de las zonas más vulnerables de Caracas: San Agustín del Sur. 
                        <br>
                        <br>
                        Verónica se abrazó y dedicó al voluntariado para alcanzar su anhelo. Califica los inicios como “rudos”, en virtud de que eran más los deseos, los proyectos y los sueños, que el recurso humano. Con el interés de ir creciendo y alcanzar éxitos, relató con mucho entusiasmo que, el padre Honegger buscó ayuda con estudiantes del Liceo Padre Machado de Fe y Alegría. “Fuimos sumando, creciendo y dándole cuerpo a la misión con la que nació Funda Epékeina”. 
                        <br>
                        <br>

                        Su buen accionar le permitió conquistar la ayuda de los voluntarios y poco a poco fueron logrando la atención de niños y jóvenes en el sector Terrazas del Alba, San Agustín del Sur. A Verónica le resulta gratificante que en corto tiempo agruparan a más de 33 niños. Eso, hace que se emocione cuando habla y recuerda cada una de las historias y gratos momentos que les dejaron sus “hijos”. Pues, así se refiere al hablar de quienes de forma recíproca conjugaron valores como amor, solidaridad y respeto. 
                        <br>
                        <br>
                        “Fueron dos años y medio de un maravilloso trabajo. Hacía de todo. Con dedicación y mucho cariño fui catequista, recreadora, organizaba juegos, enseñé a leer la Biblia y en lo posible les ofrecíamos alimentos a los niños”, relató. Las actividades las desarrollaba todos los sábados, desde las 8:00 de la mañana hasta el mediodía, en el estacionamiento de una de las Torres de Terrazas del Alba. 
                    </p>
                </div>
